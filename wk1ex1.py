from math import factorial, sqrt

print('Nul', 4 - 4 - 4 + 4)
print('Een', 4 / 4 * 4 / 4)
print('Twee', 4 / 4 + 4 / 4)
print('Drie', (4 + 4 + 4) / 4)
print('Vier', factorial(4) - (4 * 4) - 4)
print('Vijf', (4 * 4 + 4) / 4)
print('Zes', (4 + 4) / 4 + 4)
print('Zeven', 44 / 4 - 4)
print('Acht', 4 + 4 * 4 / 4)
print('Negen', 4 + 4 + 4 / 4)
print('Tien', (44 - 4) / 4)
print('Elf', 44 / (sqrt(4) + sqrt(4)))
print('Twaalf', 4 * (4 - 4 / 4))
print('Dertien', factorial(4) - 44 / 4)
print('Viertien', 4 + 4 + 4 + sqrt(4))
print('Vijftien', 4 * 4 - 4 / 4)
print('Zestien', 4 * 4 * (4 / 4))
print('Zeventien', 4 * 4 + 4 / 4)
print('Achtien', 4 * 4 + 4 / sqrt(4))
print('Negentien', factorial(4) - 4 - 4 / 4)
print('Twintig', 4 * (4 / 4 + 4))
